# variable naming rules:
# 1. Must start with a letter a-z,A-Z or an underscore _
# 2. Can not start with a number 0-9
# 3. Can contain letters, numbers and underscores
# 4. names are case sensitive (age and Age and AGE are different)
# 5. cannot use reserved keywords (for, if, while, print, etc.)

# Best practices (PEP 8): Use snake_case: All lowercase seperated by underscore