# The Building Blocks
# What is function?
# A function is a reusable block of code that performs a specific task. Istead of writing the same code again and again many times, define the function once and "call" it whenever you need it.
# -> DRY Principle - Don't Repeat Yourself. Function allow to modularize the code.
# Abstraction - Hiding the complex implementation details and showing only the essential features of the object. you don't need to know how the function works internally to use it.

# Syntax: We use def keyword to define a function.
def greet():
    print("Hello! Welcome Back.")

# Calling the function
greet()

# Naming - snake_case for function names.
# indentation - The code block inside the function must be indented.